#!/usr/bin/env node

import crypto from "node:crypto";

const baseUrl = (process.env.SUBSCRIPT_BASE_URL || "http://127.0.0.1:3000").replace(/\/$/, "");
const secretKey = process.env.SUBSCRIPT_SECRET_KEY || "";
const webhookSecret = process.env.SUBSCRIPT_WEBHOOK_SECRET || "whsec_local_smoke_secret";

function signWebhookPayload(payload, secret) {
  const timestamp = Math.floor(Date.now() / 1000);
  const body = JSON.stringify(payload);
  const signature = crypto
    .createHmac("sha256", secret)
    .update(`${timestamp}.${body}`)
    .digest("hex");

  return {
    body,
    signatureHeader: `t=${timestamp},v1=${signature}`,
  };
}

function verifyWebhookPayload(body, signatureHeader, secret, toleranceSeconds = 300) {
  const parts = Object.fromEntries(
    signatureHeader.split(",").map((part) => {
      const [key, value] = part.split("=");
      return [key, value];
    }),
  );

  if (!parts.t || !parts.v1) return false;
  const timestamp = Number(parts.t);
  if (!Number.isFinite(timestamp)) return false;
  if (Math.abs(Math.floor(Date.now() / 1000) - timestamp) > toleranceSeconds) return false;

  const expected = crypto
    .createHmac("sha256", secret)
    .update(`${parts.t}.${body}`)
    .digest("hex");

  const received = Buffer.from(parts.v1, "hex");
  const expectedBuffer = Buffer.from(expected, "hex");
  return received.length === expectedBuffer.length && crypto.timingSafeEqual(received, expectedBuffer);
}

async function smokeCreateIntent() {
  if (!secretKey) {
    return {
      skipped: true,
      reason: "Set SUBSCRIPT_SECRET_KEY to exercise POST /api/intent against a running app.",
    };
  }

  const response = await fetch(`${baseUrl}/api/intent`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${secretKey}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      title: "SubScript Integration Smoke",
      description: "Generated by scripts/subscript-integration-smoke.mjs",
      amountUsdc: "1000000",
      externalReference: `smoke-user-${Date.now()}`,
      idempotencyKey: `smoke-${Date.now()}`,
      sandbox: true,
    }),
  });

  const payload = await response.json().catch(() => ({}));
  if (!response.ok || !payload.success || !payload.intent?.checkoutUrl || !payload.intent?.receiptToken) {
    throw new Error(`Intent smoke failed: ${response.status} ${JSON.stringify(payload)}`);
  }

  return {
    skipped: false,
    status: response.status,
    checkoutUrl: payload.intent.checkoutUrl,
    receiptToken: payload.intent.receiptToken,
  };
}

async function main() {
  const webhookPayload = {
    id: "evt_payment_smoke",
    event: "payment.success",
    type: "payment.succeeded",
    created: Math.floor(Date.now() / 1000),
    data: {
      intent_id: "checkout_smoke_123",
      checkout_session_id: "checkout_smoke_123",
      merchant_reference: "user_smoke_123",
      amount: "1.00",
      amount_paid: "1.00",
      currency: "USDC",
      receipt_id: "Smoke-Receipt-abc123",
      transaction_hash: "0x" + "1".repeat(64),
    },
  };

  const signed = signWebhookPayload(webhookPayload, webhookSecret);
  const webhookSignatureValid = verifyWebhookPayload(signed.body, signed.signatureHeader, webhookSecret);
  if (!webhookSignatureValid) {
    throw new Error("Local webhook signature verification failed.");
  }

  const intentSmoke = await smokeCreateIntent();
  console.log(
    JSON.stringify(
      {
        webhookSignatureValid,
        canonicalWebhookEvent: webhookPayload.event,
        intentSmoke,
      },
      null,
      2,
    ),
  );
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
